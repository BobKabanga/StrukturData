package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 5 + 1;
        System.out.println("Hasil 5 + 1 " + hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");;
        int hsailPengurangan = 4 - 1;
        System.out.println("Haisl 4-1 = " + hsailPengurangan);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilPembagian = 15 / 6;
        System.out.println("Hasil Pembagian 15:6 = " + hasilPembagian);
        System.out.println();

        System.out.println("Operasi Habis Bagi");
        int hasilSisa = 8 % 2;
        System.out.println("Haisl 8 % 2 = " + hasilSisa);
        System.out.println();

    }
}
